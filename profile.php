<p>
  CLOTHESIS was founded in 2013 in Pelaihari, South Kalimantan, by a group of local fashion enthusiasts with a vision to create elegant, modern, and comfortable women’s clothing. Starting from a small boutique in a corner of the city, CLOTHESIS has grown into a well-known local fashion brand recognized for its classic-modern designs that combine feminine touches with local cultural values.
</p>
<p>
  Our philosophy is simple yet powerful: delivering elegance in simplicity. CLOTHESIS combines high-quality fabrics, elegant cuts, and neat stitching details to produce clothing collections that are not only beautiful but also functional. We believe that clothing can empower women to walk confidently through every step of their lives.
</p>
<p>
  Since its inception, CLOTHESIS has produced a variety of collections including daily dresses, women’s office wear, evening outfits, and modern Muslim fashion. Our designs are tailored to the lifestyle of today’s Indonesian women: active, independent, and graceful in appearance.
</p>
<p>
  Alongside technological developments and changing consumer behavior, CLOTHESIS began leveraging digital platforms to reach a wider audience. In 2020, we expanded our product distribution through online marketplaces and social media, and in 2023, we launched an official website as a hub for information, promotions, and an online product showcase.
</p>
<p>
  Beyond business, CLOTHESIS is committed to empowering women. We regularly collaborate with local creative communities and organize sewing workshops for housewives and young women. We believe that through fashion, women can express their creativity and empower themselves.
</p>
<p>
  Today, CLOTHESIS has become one of the fastest-growing local brands, with customers spread across various regions of Indonesia. With a spirit of innovation and dedication to quality, we strive to bring the CLOTHESIS name as a proud icon of Indonesian women’s fashion that can compete on an international stage.
</p>

